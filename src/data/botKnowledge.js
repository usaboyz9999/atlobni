// ── الدالة الرئيسية المحسّنة ──────────────────────────────────
export function getBotReply(input) {
  const q = (input || '').toLowerCase().trim();
  if (!q) return KB.find(k => k.id === 'default').reply;

  // حساب نقاط لكل عنصر في قاعدة المعرفة
  function scoreItem(item) {
    if (!item.keys || !item.keys.length) return 0;
    let score = 0;
    for (const kw of item.keys) {
      const kwLower = kw.toLowerCase();
      if (q === kwLower) score += 100;        // تطابق كامل
      else if (q.includes(kwLower)) score += kwLower.split(' ').length * 10; // عبارة متعددة الكلمات
      else if (kwLower.includes(q)) score += 5; // الاستعلام جزء من الكلمة المفتاحية
    }
    return score;
  }

  // البحث في KB الأساسي
  let best = null;
  let bestScore = 0;

  for (const item of KB) {
    const s = scoreItem(item);
    if (s > bestScore) { bestScore = s; best = item; }
  }

  // البحث في KB_EXTRA
  for (const item of KB_EXTRA) {
    const s = scoreItem(item);
    if (s > bestScore) { bestScore = s; best = item; }
  }

  if (best && bestScore > 0) return best.reply;
  return KB.find(k => k.id === 'default').reply;
}